/* ===== 切换逻辑（保持不变） ===== */
function _$k(){return document.querySelectorAll('[data-i18n]')}
function renderLang(lang){
  var t=L[lang]||L.zh;
  var ti=document.getElementsByTagName('title')[0];
  if(ti)ti.textContent=t['site-title'];
  _$k().forEach(function(el){
    var k=el.getAttribute('data-i18n');
    if(!k)return;
    if(typeof t[k]!=='string')return;
    el.innerHTML=t[k];
  });
  var btn=document.getElementById('langBtn');
  if(btn)btn.textContent=(lang==='zh'?'EN':'中文');
  document.documentElement.lang=lang;
  try{localStorage.setItem('rolin-lang',lang)}catch(e){}
}

(function(){
  var saved='';
  try{saved=localStorage.getItem('rolin-lang')}catch(e){}
  var lang=(saved==='en'?'en':'zh');
  renderLang(lang);
  var btn=document.getElementById('langBtn');
  if(btn)btn.onclick=function(){
    lang=lang==='zh'?'en':'zh';
    renderLang(lang);
  };
})();
